#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	int i;
	int o;

	i = 0;
	o = 0;
	while (s1[i])
		i++;
	while (s2[o])
	{
		s1[i] = s2[o];
		o++;
		i++;
	}
	s1[i] = '\0';
	return (s1);
}
